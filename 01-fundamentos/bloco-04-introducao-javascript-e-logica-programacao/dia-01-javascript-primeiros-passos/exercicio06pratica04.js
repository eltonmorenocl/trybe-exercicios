// 4. Faça um programa que, dado um valor definido numa constante, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.

const a = -3;

if(a >= 1){
    console.log("positive");
}
else if (a <= -1){
    console.log("negative");
}
else if (a == 0) {
    console.log ("zero");
}